""" Faça um Programa que peça as 4 notas bimestrais e mostre a média. """

nota1 = float(input('MÉDIA 01º BIMESTRE: '))
nota2 = float(input('MÉDIA 02º BIMESTRE: '))
nota3 = float(input('MÉDIA 03º BIMESTRE: '))
nota4 = float(input('MÉDIA 04º BIMESTRE: '))

media = (nota1 + nota2 + nota3 + nota4) / 4

print(f'A média é {media}')

