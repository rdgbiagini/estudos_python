""" Escreva um programa que leia quanto dinheiro a pessoa tem na carteira e diga a ela quantos dólares é possível ela comprar. 
Considerando o dólar a R$ 3.27 """

dinheiro = float(input('Quanto dinheiro você tem na carteira? '))

dolar = 3.17

print(f'Considerando que você tem {dinheiro} e que o dólar está a {dolar}, você pode comprar aproximadamente {dinheiro / dolar}')
